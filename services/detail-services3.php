        // Obtener una lista de todos los elementos con clase 'wrap-services'
        const wrapServicesList = document.querySelectorAll('.wrap-services');

        // Iterar sobre la lista y agregar event listeners a cada elemento
        wrapServicesList.forEach(wrapServices => {
            wrapServices.addEventListener('mouseenter', () => {
                wrapServices.addEventListener('mousemove', onMouseMove); // Agregar oyente de evento para el movimiento del ratón
            });

            wrapServices.addEventListener('mouseleave', () => {
                wrapServices.removeEventListener('mousemove', onMouseMove); // Eliminar oyente de evento para el movimiento del ratón
                wrapServices.style.transform = ''; // Restaurar la transformación a su estado original cuando el ratón sale del elemento
            });
        });

        function onMouseMove(e) {
            const boundingRect = this.getBoundingClientRect();
            const mouseX = e.clientX - boundingRect.left; // Posición X del cursor dentro del elemento
            const mouseY = e.clientY - boundingRect.top; // Posición Y del cursor dentro del elemento

            const percentX = (mouseX / boundingRect.width - 0.5) * 10; // Porcentaje de posición X del cursor
            const percentY = (mouseY / boundingRect.height - 0.5) * 10; // Porcentaje de posición Y del cursor

            this.style.transform = `perspective(80px) rotateX(${-percentY * 0.2}deg) rotateY(${percentX * 0.2}deg)`; // Aplicar la transformación
